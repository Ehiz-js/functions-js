function leapYear(year) {
	if (year % 4 === 0 && year % 400 === 0) {
		console.log("Leap year.");
	} else if (year % 4 === 0) {
		console.log("Leap year.");
	} else {
		console.log("Not leap year.");
	}
}
console.log(leapYear(1948));

